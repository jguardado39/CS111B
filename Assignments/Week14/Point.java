package Assignments.Week14;

public class Point {
    private int x;
    private int y;

    public Point() {
        x = 1;
        y = 1;
    }


    public Point(int a, int b) {
        x = a;
        y = b;
    }


    public int getX() {
        return x;
    }
    

    public int getY() {
        return y;
    }
}
